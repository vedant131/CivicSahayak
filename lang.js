// ── Language engine ────────────────────────────
window.currentLang = 'en';

window.setLang = function(code) {
  window.currentLang = code;
  const T = window.LANG[code];
  if (!T) return;

  // Helper: set text safely
  const setText = (id, val) => { const el = document.getElementById(id); if (el && val !== undefined) el.innerHTML = val; };
  const setAll  = (cls, val) => { document.querySelectorAll('.' + cls).forEach(el => { if (val !== undefined) el.innerHTML = val; }); };

  // Sidebar nav labels
  const navMap = {
    home: T.nav_home, eligibility: T.nav_eligibility, documents: T.nav_documents,
    rights: T.nav_rights, complaint: T.nav_complaint, legal: T.nav_legal_p,
    grievance: T.nav_grievance, tracker: T.nav_tracker, helplines: T.nav_helplines, ai: T.nav_ai,
  };
  document.querySelectorAll('.ni[data-p]').forEach(el => {
    const p = el.dataset.p;
    if (navMap[p]) {
      const ico = el.querySelector('.ni-ico');
      el.textContent = navMap[p];
      if (ico) el.prepend(ico);
    }
  });

  // Section labels
  document.querySelectorAll('.slbl').forEach((el, i) => {
    el.textContent = i === 0 ? T.nav_main : T.nav_legal;
  });

  // Sidebar footer
  setText('sb-notice-h', T.sb_notice);
  setText('sb-notice-b', T.sb_notice2);
  setText('sb-tag-el', T.sb_tag);

  // Topbar buttons
  setText('btn-helplines', T.bar_helplines);
  setText('btn-documents', T.bar_documents);
  setText('btn-ai', T.bar_ai);

  // Topbar title
  const titleEl = document.getElementById('abar-title');
  if (titleEl && T.titles[window.currentLang === code ? window.currentPanel || 'home' : 'home']) {
    titleEl.textContent = T.titles[window.currentPanel || 'home'];
  }

  // Ticker
  const inner = document.querySelector('.ticker-inner');
  if (inner && T.ticker) {
    inner.innerHTML = T.ticker.map(t =>
      `<span class="ticker-item">${t}</span><span class="ticker-sep">◆</span>`
    ).join('') + T.ticker.map(t =>
      `<span class="ticker-item">${t}</span><span class="ticker-sep">◆</span>`
    ).join('');
  }

  // Hero
  setText('hero-badge', T.hero_badge);
  setText('hero-h', T.hero_h);
  setText('hero-sub', T.hero_sub);
  setText('hero-btn1', T.hero_btn1);
  setText('hero-btn2', T.hero_btn2);
  setText('hero-btn3', T.hero_btn3);

  // Problems
  setText('prob-label', T.prob_label);
  setText('prob1-t', T.prob1_t); setText('prob1-d', T.prob1_d);
  setText('prob2-t', T.prob2_t); setText('prob2-d', T.prob2_d);
  setText('prob3-t', T.prob3_t); setText('prob3-d', T.prob3_d);

  // Stats
  setText('stat1-lbl', T.stat1_lbl); setText('stat2-lbl', T.stat2_lbl);
  setText('stat3-lbl', T.stat3_lbl); setText('stat4-lbl', T.stat4_lbl);

  // Feature tiles
  for (let i = 1; i <= 6; i++) {
    setText(`ft${i}-t`, T[`ft${i}_t`]);
    setText(`ft${i}-d`, T[`ft${i}_d`]);
  }

  // Eligibility
  setText('elig-label', T.elig_label);
  setText('elig-h', T.elig_h);
  setText('elig-sub', T.elig_sub);
  setText('elig-btn', T.elig_btn);
  setText('elig-result-title', T.elig_result_title);
  ['cat','income','edu','gender','state','age'].forEach(f => {
    const el = document.getElementById(`flbl-${f}`);
    if (el) el.textContent = T[`elig_${f}`];
  });

  // Documents
  setText('doc-label', T.doc_label);
  setText('doc-h', T.doc_h);
  setText('doc-sub', T.doc_sub);

  // Rights
  setText('rights-label', T.rights_label);
  setText('rights-h', T.rights_h);
  setText('rights-sub', T.rights_sub);
  for (let i = 1; i <= 6; i++) {
    setText(`r${i}-t`, T[`r${i}_t`]);
    setText(`r${i}-d`, T[`r${i}_d`]);
    const link = document.getElementById(`r${i}-link`);
    if (link) link.textContent = T.r_read;
  }

  // Wizard
  setText('wiz-label', T.wiz_label);
  setText('wiz-h', T.wiz_h);
  setText('wiz-sub', T.wiz_sub);
  if (T.wiz_steps) {
    T.wiz_steps.forEach((s, i) => {
      const el = document.getElementById(`ws-lbl-${i+1}`);
      if (el) el.textContent = s;
    });
  }
  for (let i = 1; i <= 5; i++) {
    setText(`wp${i}-h`, T[`w${i}_h`]);
    if (i <= 3) setText(`wp${i}-s`, T[`w${i}_s`]);
  }
  setText('wiz-back', T.wiz_back);
  setText('wiz-next', T.wiz_next);
  setText('wiz-submit', T.wiz_submit);

  // Legal
  setText('legal-label', T.legal_label);
  setText('legal-h', T.legal_h);
  setText('legal-sub', T.legal_sub);
  setText('fir-title', T.fir_title);

  // Grievance
  setText('griev-label', T.griev_label);
  setText('griev-h', T.griev_h);
  setText('griev-sub', T.griev_sub);
  document.querySelectorAll('.gi-link').forEach(el => el.textContent = T.griev_visit);

  // Tracker
  setText('tracker-label', T.tracker_label);
  setText('tracker-h', T.tracker_h);
  setText('tracker-sub', T.tracker_sub);

  // Helplines
  setText('help-label', T.help_label);
  setText('help-h', T.help_h);
  setText('help-sub', T.help_sub);
  document.querySelectorAll('.hc-call').forEach(el => el.textContent = T.help_call);

  // AI
  setText('ai-name', T.ai_name);
  setText('ai-status-txt', T.ai_status);
  setText('ai-badge', T.ai_badge);
  const aiInp = document.getElementById('ai-inp');
  if (aiInp) aiInp.placeholder = T.ai_placeholder;
  setText('ai-send-btn', T.ai_send);
  setText('ai-disclaimer', T.ai_disclaimer);

  // Update lang button states
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === code);
  });

  // Animate the transition
  document.querySelector('.main').style.animation = 'none';
  requestAnimationFrame(() => {
    document.querySelector('.main').style.animation = 'fadeIn .3s ease';
  });
};

// Track current panel for title updates
window.currentPanel = 'home';
const _origGo = window.go;
if (_origGo) {
  window.go = function(id) {
    window.currentPanel = id;
    _origGo(id);
    const T = window.LANG[window.currentLang];
    const titleEl = document.getElementById('abar-title');
    if (titleEl && T && T.titles && T.titles[id]) {
      titleEl.textContent = T.titles[id];
    }
  };
}
